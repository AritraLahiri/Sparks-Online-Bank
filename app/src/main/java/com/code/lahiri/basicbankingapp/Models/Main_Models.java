package com.code.lahiri.basicbankingapp.Models;

public class Main_Models {

    int pic ;
    String name;

    public Main_Models(int pic, String name) {
        this.pic = pic;
        this.name = name;
    }

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
